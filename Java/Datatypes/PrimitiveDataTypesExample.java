package Datatypes;

public class PrimitiveDataTypesExample {
    public static void main(String[] args) {
        byte age = 25;
        short score = 32000;
        int count = 1000;
        long population = 8000000000L;

        System.out.println(age);
        System.out.println(score);
        System.out.println(count);
        System.out.println(population);
    }
}