package Variables;

public class DefaultValuesDemo {
    int defaultInt;
    boolean defaultBool;
    double defaultDouble;
    String defaultString;

    public static void main(String[] args) {
        DefaultValuesDemo demo = new DefaultValuesDemo();
        System.out.println("Default int: " + demo.defaultInt);
        System.out.println("Default boolean: " + demo.defaultBool);
        System.out.println("Default double: " + demo.defaultDouble);
        System.out.println("Default String: " + demo.defaultString);
    }
}
